#include <stdio.h>

int main(){
    int num = 12;

    // Printing Decimal Number in different bases
    printf("In decimal Form : %d\n", num); //decimal
    printf("In Octal Form: %o\n", num); // Octal
    printf("In hex lower form: %x\n", num);// hex lower 
    printf("In hex upper form: %X", num); // hex upper
    
    return 0;
}